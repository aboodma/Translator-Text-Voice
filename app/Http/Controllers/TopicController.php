     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       $topic = new Topic();
       $topic->text = $request->text;
       $topic->category_id = $request->category_id;
       if ($topic->save()){
           return redirect()->route('topics.index');
       }
    }

